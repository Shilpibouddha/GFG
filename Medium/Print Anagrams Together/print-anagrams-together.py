class Solution:
    def Anagrams(self, words, n):
        '''
        words: list of word
        n:      no of words
        return : list of group of anagram {list will be sorted in driver code (not word in grp)}
        '''

        dict1 = {}
        for i in words:
            group = ''.join(sorted(i))
            if group not in dict1:
                dict1[group] = [i]
            else:
                dict1[group].append(i)
        return dict1.values()


